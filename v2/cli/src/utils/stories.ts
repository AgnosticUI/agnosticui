function toKebabCase(str: string): string {
  return str.replace(/([A-Z])/g, (_m, p1, offset) => (offset > 0 ? '-' : '') + p1.toLowerCase());
}

function getAgTagName(name: string): string {
  // Special overrides
  if (name === 'Flex') return 'ag-flex-row';
  return `ag-${toKebabCase(name)}`;
}

